// -- incrementation c'est ajouter 1
var nb1 = 1;
// nb1 = nb1 + 1;
nb1 += 1; // ajoute 1

console.log(nb1); // nb1 = 2;

// ecriture simplifiée on peux fair:
nb1++
console.log(nb1); // nb1 = 3;

++nb1;
console.log(nb1); // nb1 = 4;

// la derementation c'est retiré 1
nb1 = nb1 - 1;
// nb1 -= 1;
console.log(nb1); // nb1 = 3;

// ecriture simplifiée :
nb1--;
console.log(nb1); // nb1 = 2;

--nb1;
console.log(nb1); // nb1 = 1;